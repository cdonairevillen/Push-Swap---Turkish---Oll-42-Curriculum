#include "libft.h"

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned char	*str1;
	unsigned char	*str2;

	i = 0;
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (str1[i] || str2[i])
	{
		if (str1[i] == str2[i] && i < n - 1)
			i++;
		else
			return (str1[i] - str2[i]);
	}
	return (0);
}
